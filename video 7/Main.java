



public class Main 
{
    public static void main(String[] args) 
    {
        int num1 = 6;
        float num2 = 5f;


        // <
        if (num1 < num2) 
        {
            System.out.println("num1 < num2");
        } 
        else 
        {
            System.out.println("num1 >= num2");
        }
        

        // <=
        if (num1 <= num2) 
        {
            System.out.println("num1 <= num2");
        } 
        else 
        {
            System.out.println("num1 > num2");
        }


        // >
        if (num1 > num2) 
        {
            System.out.println("num1 > num2");
        } 
        else 
        {
            System.out.println("num1 <= num2");
        }


        // >=
        if (num1 >= num2) 
        {
            System.out.println("num1 >= num2");
        } 
        else 
        {
            System.out.println("num1 < num2");
        }


        // ==
        if (num1 == num2) 
        {
            System.out.println("num1 == num2");
        } 
        else 
        {
            System.out.println("num1 != num2");
        }


        // !=
        if (num1 != num2) 
        {
            System.out.println("num1 != num2");
        } 
        else 
        {
            System.out.println("num1 == num2");
        }
    }
}